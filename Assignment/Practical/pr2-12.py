# 12. Write a Python program to access values using dictionary keys.

# Define a dictionary
student = {
    "name": "Pruthvi",
    "age": 19,
    "grade": "A",
    "subject": "Science"
}

# Access values using keys
print("Student Name:", student["name"])
print("Age:", student["age"])
print("Grade:", student["grade"])
print("Subject:", student["subject"])